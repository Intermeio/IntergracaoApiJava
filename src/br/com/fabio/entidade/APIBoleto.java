package br.com.fabio.entidade;

public class APIBoleto {

	public String hashBoleto;
	public String hashSimplificado;
	public String linhaDigitavel;
	public String codigoDeBarras;
	public String nossoNumero;
	public String refNumeroDocumento;
	public String refToken;
	public String split;

	public String getHashBoleto() {
		return hashBoleto;
	}

	public void setHashBoleto(String hashBoleto) {
		this.hashBoleto = hashBoleto;
	}

	public String getHashSimplificado() {
		return hashSimplificado;
	}

	public void setHashSimplificado(String hashSimplificado) {
		this.hashSimplificado = hashSimplificado;
	}

	public String getLinhaDigitavel() {
		return linhaDigitavel;
	}

	public void setLinhaDigitavel(String linhaDigitavel) {
		this.linhaDigitavel = linhaDigitavel;
	}

	public String getCodigoDeBarras() {
		return codigoDeBarras;
	}

	public void setCodigoDeBarras(String codigoDeBarras) {
		this.codigoDeBarras = codigoDeBarras;
	}

	public String getNossoNumero() {
		return nossoNumero;
	}

	public void setNossoNumero(String nossoNumero) {
		this.nossoNumero = nossoNumero;
	}

	public String getRefNumeroDocumento() {
		return refNumeroDocumento;
	}

	public void setRefNumeroDocumento(String refNumeroDocumento) {
		this.refNumeroDocumento = refNumeroDocumento;
	}

	public String getRefToken() {
		return refToken;
	}

	public void setRefToken(String refToken) {
		this.refToken = refToken;
	}

	public String getSplit() {
		return split;
	}

	public void setSplit(String split) {
		this.split = split;
	}

}
